window.onload = function() {
    // Get the selected language from localStorage
    const savedLanguage = localStorage.getItem('selectedLanguage') || 'pt';  // Default to 'pt' if not set
    setLanguage(savedLanguage);
};

function changeLanguage(language) {
    if (!translations[language]) {
        console.error(`Language ${language} not found!`);
        return; // Exit the function if the language is not valid
    }

    // Save the language selection in localStorage
    localStorage.setItem('selectedLanguage', language);

    // Update the page language immediately
    setLanguage(language);
}





        // Função para verificar se o elemento está visível na viewport
        function isInViewport(element) {
            const rect = element.getBoundingClientRect();
            return rect.top < window.innerHeight && rect.bottom > 0;
        }

        // Verifica os elementos a cada rolagem
        function checkVisibility() {
            const elements = document.querySelectorAll('.img1, .foto, .text, .image-container');
            elements.forEach((element) => {
                if (isInViewport(element)) {
                    element.classList.add('show'); // Adiciona a classe 'show'
                }
            });
        }

        // Ativa o efeito na rolagem e no carregamento inicial
        window.addEventListener('scroll', checkVisibility);
        window.addEventListener('load', checkVisibility);

